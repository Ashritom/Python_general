len=int(input("Enter the length of rectangle: "))
wid=int(input("Enter the width of rectangle: "))
perimeter=2*(len+wid)
area=len*wid
print("Perimeter of the rectangle: ",perimeter)
print("Area of a rectangle: ",area)
print("\n")
